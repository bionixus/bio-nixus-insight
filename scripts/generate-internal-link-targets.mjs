#!/usr/bin/env node
/**
 * Parses scripts/data/internal-link-report.csv (URL,"Internal links")
 * for rows with internal link count &lt; 5, then writes
 * src/lib/lowInternalLinkTargets.generated.ts for sitewide internal link amplification.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const csvPath = path.join(root, 'scripts', 'data', 'internal-link-report.csv');
const outPath = path.join(root, 'src', 'lib', 'lowInternalLinkTargets.generated.ts');

function pathFromUrl(urlStr) {
  try {
    const u = new URL(urlStr.trim());
    if (!u.hostname.includes('bionixus.com')) return null;
    let p = u.pathname.replace(/\/+$/, '') || '/';
    if (p !== '/') {
      try {
        const segments = p.split('/').map((seg, i) => {
          if (i === 0 || seg === '') return seg;
          return decodeURIComponent(seg);
        });
        p = segments.join('/');
      } catch {
        try {
          p = decodeURIComponent(p);
        } catch {
          /* keep encoded segments */
        }
      }
    }
    return p === '' ? '/' : p;
  } catch {
    return null;
  }
}

/** Title-case-ish label without breaking Arabic slug segments too aggressively */
function pathnameToLabel(pathname) {
  if (pathname === '/') return 'Home';
  if (pathname === '/ar') return 'BioNixus — Arabic homepage';
  if (pathname === '/de') return 'BioNixus — German homepage';
  if (pathname === '/fr') return 'BioNixus — French homepage';
  if (pathname === '/es') return 'BioNixus — Spanish homepage';
  if (pathname === '/zh') return 'BioNixus — Chinese homepage';

  const tail = pathname.split('/').filter(Boolean).pop() ?? pathname;
  const decodedTail = (() => {
    try {
      return decodeURIComponent(tail);
    } catch {
      return tail;
    }
  })();
  if (/[\u0600-\u06FF]/.test(decodedTail)) {
    return 'Healthcare market research insight — Arabic blog article';
  }
  return decodedTail.replace(/-/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase());
}

function parseReportRows(text) {
  const lines = text.split(/\r?\n/).filter((l) => l.trim().length > 0);
  if (!lines.length) return [];
  const header = lines[0];
  if (!/^URL/i.test(header)) {
    console.warn('[generate-internal-link-targets] unexpected header:', header);
  }
  const rows = [];
  for (let i = 1; i < lines.length; i++) {
    const line = lines[i];
    const lastComma = line.lastIndexOf(',');
    if (lastComma === -1) continue;
    const urlRaw = line.slice(0, lastComma).trim();
    const countRaw = line.slice(lastComma + 1).replace(/"/g, '').trim();
    const count = Number.parseInt(countRaw, 10);
    if (!Number.isFinite(count)) continue;
    rows.push({ url: urlRaw, count });
  }
  return rows;
}

function main() {
  if (!fs.existsSync(csvPath)) {
    throw new Error(`internal-link-report.csv missing: ${csvPath}`);
  }
  const csvText = fs.readFileSync(csvPath, 'utf8').replace(/^\uFEFF/, '');
  const parsed = parseReportRows(csvText);
  const threshold = 5;
  /** @type {Map<string,string>} */
  const byPath = new Map();

  for (const { url, count } of parsed) {
    if (!Number.isFinite(count) || count >= threshold) continue;
    const to = pathFromUrl(url);
    if (!to || to === '/') continue;
    if (!byPath.has(to)) {
      byPath.set(to, pathnameToLabel(to));
    }
  }

  const sorted = [...byPath.entries()].sort((a, b) => a[0].localeCompare(b[0]));
  const items = sorted.map(([to, label]) => {
    const line = JSON.stringify({ to, label });
    return `  ${line},`;
  });

  const file = `/* eslint-disable prettier/prettier */
/**
 * Auto-generated by scripts/generate-internal-link-targets.mjs — do not edit by hand.
 * Source: scripts/data/internal-link-report.csv (rows with fewer than 5 inbound internal links).
 * Regenerate: npm run generate-internal-link-targets
 */

export interface LowInternalLinkTarget {
  readonly to: string;
  readonly label: string;
}

/** Paths that need inbound internal-link support (from latest CSV crawl export). */
export const LOW_INTERNAL_LINK_TARGETS: readonly LowInternalLinkTarget[] = [
${items.join('\n')}
];

export const LOW_INTERNAL_LINK_PATHS: readonly string[] = LOW_INTERNAL_LINK_TARGETS.map((t) => t.to);
`;

  fs.writeFileSync(outPath, file, 'utf8');
  console.info(`[generate-internal-link-targets] wrote ${sorted.length} targets → ${path.relative(root, outPath)}`);
}

main();
